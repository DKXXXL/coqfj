(* WFCT is basically saying classdecls are acyclic *)
Inductive WFCT : [ClassDecl] -> Prop :=
  | wfct_nil : WFCT nil 
  | wfct_cons:
      forall C D Fs noDupfs K Ms noDupMds l,
        (D = Object \/ find D l <> None) -> (* D exists in the list *)
        WFCT l ->
        WFCT ((CDecl C D Fs noDupfs K Ms noDupMds)::l).


Module WFCT_DEC.

Theorem wfct_dec :
  forall CT,
    {WFCT CT} + {~WFCT CT}.
Admitted.

End WFCT_DEC.

